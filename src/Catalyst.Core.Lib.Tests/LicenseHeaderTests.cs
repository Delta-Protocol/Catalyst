using System;
using System.IO;
using System.Linq;
using System.Reflection;
using System.Text.RegularExpressions;
using System.Threading.Tasks;
using Catalyst.TestUtils;
using FluentAssertions;
using Xunit;
using Xunit.Abstractions;

namespace Catalyst.Core.Lib.Tests
{
    public class LicenseHeaderTests : FileSystemBasedTest
    {
        public LicenseHeaderTests(ITestOutputHelper output) : base(output) { }
        private const string LicenseHeaderFileName = "COPYING";

        [Fact]
        public static async Task All_Cs_Files_Should_Have_License_Header()
        {
            var declaringTypeAssembly = MethodBase.GetCurrentMethod().DeclaringType?.Assembly;

            var basePathRegex = new Regex("(?<sourcePath>(.*)[\\/]src[\\/])(.*)");
            var codeBasePath = new Uri(declaringTypeAssembly?.CodeBase).AbsolutePath;
            var sourcePath = basePathRegex.Match(codeBasePath).Groups["sourcePath"].Value;

            sourcePath.Should().NotBeNull();
            var sourceDirectory = new DirectoryInfo(sourcePath);

            var copyingText = (await File.ReadAllTextAsync(Path.Combine(sourceDirectory.Parent?.FullName, LicenseHeaderFileName)))
               .TrimEndOfLines();
            copyingText.Should().StartWith(@"#region LICENSE");

            var getWrongFiles = sourceDirectory.EnumerateFiles("*.cs", SearchOption.AllDirectories)
               .Where(f => !f.Name.Contains(".g.cs"))
               .Where(f => !f.Name.Contains(".AssemblyInfo.cs"))
               .Where(f => f.Directory?.Parent?.Parent?.Name != "obj")
               .Where(f => f.Directory?.Parent?.Parent?.Name != "bin")
               .Select(async f =>
                {
                    var allText = (await File.ReadAllTextAsync(f.FullName)).TrimEndOfLines();
                    return allText.StartsWith(copyingText) || allText.StartsWith("// <auto-generated>") ? null : f.FullName;
                }).ToArray();

            var wrongFiles = (await Task.WhenAll(getWrongFiles)).Where(f => f != null).ToArray();
            foreach (var wrongFile in wrongFiles)
            {
                var content = File.ReadAllText(wrongFile);
                var s =
                    "#region LICENSE\n\n/**\n* Copyright (c) 2019 Catalyst Network\n*\n* This file is part of Catalyst.Node <https://github.com/catalyst-network/Catalyst.Node>\n*\n* Catalyst.Node is free software: you can redistribute it and/or modify\n* it under the terms of the GNU General Public License as published by\n* the Free Software Foundation, either version 2 of the License, or\n* (at your option) any later version.\n*\n* Catalyst.Node is distributed in the hope that it will be useful,\n* but WITHOUT ANY WARRANTY; without even the implied warranty of\n* MERCHANTABILITY or FITNESS FOR A PARTICULAR PURPOSE. See the\n* GNU General Public License for more details.\n*\n* You should have received a copy of the GNU General Public License\n* along with Catalyst.Node. If not, see <https://www.gnu.org/licenses/>.\n*/\n\n#endregion\n\n" +
                    content;
                File.WriteAllText(wrongFile, s);
                var t = 0;
            }

            wrongFiles.Should().BeEmpty(
                $"all files should have a header{Environment.NewLine}" +
                $"{string.Join(Environment.NewLine, wrongFiles)} " + Environment.NewLine);
        }
    }

    internal static class StringExtensions
    {
        internal static string TrimEndOfLines(this string originalString)
        {
            return new string(originalString.Where(c => c != '\n' && c != '\r').ToArray());
        }
    }
}
